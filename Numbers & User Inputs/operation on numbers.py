# operation in numbers
# example

num1 = 5; #integer
num2 = 2; #floating point num

# operation done on these two num
result = num1 + num2;

# calculating the power of the two var
result4 = num1 ** num2;

# in case of division the result will be in floating point value 
result2 = num1 / num2;

# to get the result in integer // is used 
result3 = num1 // num2;


print(result);
print(result4);
print(result2);
print(result3);


# shortcut for number operation can be used for every operation
num1+= 2;
num1-= 2;

print(num1);
